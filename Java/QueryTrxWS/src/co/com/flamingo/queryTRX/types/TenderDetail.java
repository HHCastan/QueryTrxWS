package co.com.flamingo.queryTRX.types;

public class TenderDetail {
	private String tenderCode;
	private String signo;
	private int tenderAmnt;
	private String tenderSerial;
	
	public TenderDetail() {
		
	}

	public String getTenderCode() {
		return tenderCode;
	}

	public void setTenderCode(String tenderCode) {
		this.tenderCode = tenderCode;
	}

	public String getSigno() {
		return signo;
	}

	public void setSigno(String signo) {
		this.signo = signo;
	}

	public int getTenderAmnt() {
		return tenderAmnt;
	}

	public void setTenderAmnt(int tenderAmnt) {
		this.tenderAmnt = tenderAmnt;
	}

	public String getTenderSerial() {
		return tenderSerial;
	}

	public void setTenderSerial(String tenderSerial) {
		this.tenderSerial = tenderSerial;
	}
}
