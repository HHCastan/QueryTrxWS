package com.fla.cotizador.modelo;

import java.sql.Date;

public class ClienteSicaf {
	private String clienteID;
	private String tipoDoc;
	private String clienteName;
	private int clienteCupo;
	private String clienteBloqueo;
	private String documentacionImpresa;
	private String documentosCustodia;
	private String actualizado;
	private Date fechaActualizado;
	private String nroCelular;
	private String eMail;
	private String nug;
	private String apto;
	private int cupoMefia;
	private int creditosMefia;
	private Date proximaCuota;
	
	public ClienteSicaf(String clienteID, String tipoDoc, String clienteName, int clienteCupo, String clienteBloqueo) {
		super();
		this.clienteID = clienteID;
		this.tipoDoc = tipoDoc;
		this.clienteName = clienteName;
		this.clienteCupo = clienteCupo;
		this.clienteBloqueo = clienteBloqueo;
	}

	public ClienteSicaf() {
		super();
	}

	public String getClienteID() {
		return clienteID;
	}

	public void setClienteID(String clienteID) {
		this.clienteID = clienteID;
	}

	public String getTipoDoc() {
		return tipoDoc;
	}

	public void setTipoDoc(String tipoDoc) {
		this.tipoDoc = tipoDoc;
	}

	public String getClienteName() {
		return clienteName;
	}

	public void setClienteName(String clienteName) {
		this.clienteName = clienteName;
	}

	public int getClienteCupo() {
		return clienteCupo;
	}

	public void setClienteCupo(int clienteCupo) {
		this.clienteCupo = clienteCupo;
	}

	public String getClienteBloqueo() {
		return clienteBloqueo;
	}

	public void setClienteBloqueo(String bloqueo) {
		this.clienteBloqueo = bloqueo;
	}

	public String getDocumentacionImpresa() {
		return documentacionImpresa;
	}

	public void setDocumentacionImpresa(String documentacionImpresa) {
		this.documentacionImpresa = documentacionImpresa;
	}

	public String getDocumentosCustodia() {
		return documentosCustodia;
	}

	public void setDocumentosCustodia(String documentosCustodia) {
		this.documentosCustodia = documentosCustodia;
	}

	public String getActualizado() {
		return actualizado;
	}

	public void setActualizado(String actualizado) {
		this.actualizado = actualizado;
	}

	public Date getFechaActualizado() {
		return fechaActualizado;
	}

	public void setFechaActualizado(Date date) {
		this.fechaActualizado = date;
	}

	public String getNroCelular() {
		return nroCelular;
	}

	public void setNroCelular(String nroCelular) {
		this.nroCelular = nroCelular;
	}

	public String geteMail() {
		return eMail;
	}

	public void seteMail(String eMail) {
		this.eMail = eMail;
	}

	public String getNug() {
		return nug;
	}

	public void setNug(String nug) {
		this.nug = nug;
	}

	public String getApto() {
		return apto;
	}

	public void setApto(String apto) {
		this.apto = apto;
	}

	public int getCupoMefia() {
		return cupoMefia;
	}

	public void setCupoMefia(int cupoMefia) {
		this.cupoMefia = cupoMefia;
	}

	public int getCreditosMefia() {
		return creditosMefia;
	}

	public void setCreditosMefia(int creditosMefia) {
		this.creditosMefia = creditosMefia;
	}

	public Date getProximaCuota() {
		return proximaCuota;
	}

	public void setProximaCuota(Date proximaCuota) {
		this.proximaCuota = proximaCuota;
	}
}
